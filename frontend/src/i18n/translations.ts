/** Returns the correct locale string for Intl APIs. */
export function getLangLocale(lang: Lang): string {
  if (lang === "de") return "de-DE";
  if (lang === "es") return "es-ES";
  return "en-US";
}

/** Format a number respecting the language's decimal separator. */
export function formatNumber(val: number, lang: Lang, digits = 1): string {
  return val.toLocaleString(getLangLocale(lang), {
    minimumFractionDigits: digits,
    maximumFractionDigits: digits,
  });
}
